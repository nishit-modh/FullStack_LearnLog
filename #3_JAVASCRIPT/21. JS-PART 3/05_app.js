let cars = ["Porche", "Mclaren", "Jaguar", "Mercedes"];
console.log(cars);

// Push : add to end
cars.push("Hyundai");
console.log(cars);

// Pop : delete from end & returns it
cars.pop();
console.log(cars);

// Unshift : add to start
cars.unshift("BMW");
console.log(cars);

// Shift : delete from start & returns it
let discar = cars.shift();
console.log(discar);
console.log(cars);